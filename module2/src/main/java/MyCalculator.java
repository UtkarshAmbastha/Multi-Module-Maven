public class MyCalculator {
    void add(final int num1, final int num2) {
        int add;
        add = num1 + num2;

        System.out.println("The sum is: " +add);

    }

    void subtract(final int num1, final int num2) {
        int sub;
        sub = num1 - num2;

        System.out.println("The sub is: " +sub);
    }

    void multiplication(final  int num1, final int num2) {
        int multiply;
        multiply = num1 * num2;

        System.out.println("The sum is: " +multiply);
    }

    void division(final double num1, final double num2) {
        double division;
        division = num1 / num2;

        System.out.println("The sum is: " +division);
    }
}
